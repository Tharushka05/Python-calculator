num1 = float(input("Enter a number;"))
operation = input("Enter an operation;")
num2 = float(input("Enter another number;"))

if operation == "+":
    print(num1+num2)
if operation == "-":
    print(num1-num2)
if operation == "*":
    print(num1*num2)
if operation == "/":
    print(num1/num2)


